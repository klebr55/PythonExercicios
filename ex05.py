# Exercício Python 5: Faça um programa que leia um número
# Inteiro e mostre na tela o seu sucessor e seu antecessor.

numero = int(input("Digite um número que eu mostrarei seu\nanterior e seu sucessor: "))
print(f"O número antecessor de {numero} é: {numero - 1}\n"
      f"o número sucessor de {numero} é: {numero + 1}")